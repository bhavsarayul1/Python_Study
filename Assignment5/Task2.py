# Task2: Demonstrate List Slicing

# create list of numbers from 1 to 10

list_1 = list(range(1, 11))

# Extracts the first five elements from the list.

list_2 = list_1[:5]

# Reverse extracted elements

list_3 = list_2[::-1]

# Output
print(f"Original list: {list_1}")
print(f"Extracted first five elements: {list_2}")
print(f"Reversed extracted elements: {list_3}")



